#include <bits/stdc++.h>
using namespace std;
//Relational operators are applicable to pointers
int main(){
    int* ptr1;
    int* ptr2;
    cout<<ptr1<<endl;
    cout<<ptr2<<endl;
    cout<<(ptr1==ptr2)<<endl;
    cout<<(ptr1!=ptr2)<<endl;
    cout<<(ptr1<=ptr2)<<endl;
    cout<<(ptr1>=ptr2)<<endl;
    return 0;
}