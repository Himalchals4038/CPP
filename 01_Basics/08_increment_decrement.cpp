#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int x = 4;
    x = x + 1;
    cout<<x<<endl;
    x += 1;
    cout<<x<<endl;
    x++; //Post Increment
    cout<<x<<endl;
    x--; //Post Decrement
    cout<<x<<endl;
    ++x; //Pre Increment
    cout<<x<<endl;
    --x; //Pre Decrement
    cout<<x<<endl;

    return 0;
}